    uint8_t rx_data[4] = {0};

    HAL_SPI_TransmitReceive(&hspi1,
                            tx_data,
                            rx_data,
                            sizeof(tx_data),
                            HAL_MAX_DELAY);

    printf("SPI TX: ");
    for (uint8_t i = 0; i < sizeof(tx_data); i++)
    {
        printf("%02X ", tx_data[i]);
    }

    printf("\r\nSPI RX: ");
    for (uint8_t i = 0; i < sizeof(rx_data); i++)
    {
        printf("%02X ", rx_data[i]);
    }

    printf("\r\n\r\n");
